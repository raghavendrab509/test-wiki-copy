package com.forrent.beans.geography.entity;

class Point
{
    private Double lat;
    private Double lon;

    public Double getLat() {
        return this.lat;
    }

    public void setLat(final Double lat) {
        this.lat = lat;
    }

    public Double getLon() {
        return this.lon;
    }

    public void setLon(final Double lon) {
        this.lon = lon;
    }
}